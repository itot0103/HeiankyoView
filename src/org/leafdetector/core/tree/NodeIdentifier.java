package org.leafdetector.core.tree;

import org.heiankyoview2.applet.junihitoeview.GlDefaultCanvas;
import org.heiankyoview2.core.tree.Tree;
import org.heiankyoview2.core.tree.Branch;

/**
 * @author fyamashi
 *
 * TODO ????????????????????????????????:
 * ????? - ?? - Java - ???????? - ??????????
 */
public class NodeIdentifier {

	Tree tree=null;
	Branch nodeBranch=null;
	Node rootNode;
	GlDefaultCanvas canvas=null;
	
	public void setCanvas(GlDefaultCanvas canvas){
		this.canvas=canvas;
	}
	
	public void create(GlDefaultCanvas canvas){
		this.canvas=canvas;
		nodeBranch=canvas.getTree().getBranchAt(1);
		rootNode=new Node(nodeBranch,canvas);
	}
	
	public Branch identifyBranch(int dataID){
		return rootNode.getBranch(dataID);
	}
	
	public Branch identifyBranch(String[] data){
		return rootNode.getBranch(data);
	}
}
